    {
        $doctor = ["name" => $request->name,"hospital" => $request->hospital,"country"=>$request->country,
        "detail"=>$request->detail,"description"=>$request->description,"department"=>$request->department,
            "wtsp" =>$request->wtsp,"phone"=>$request->phone,"adress"=>$request->adress,"fee"=>$request->fee,
            "time"=>$request->time,"email"=>$request->email];
            $imagePath1 = $request->file('image')->store('public/images/doctors');
            $image1 = basename($imagePath1);
            $doctor["image"] = $image1;
            Doctor::create($doctor);
            return redirect(route('doctor.index')); 
